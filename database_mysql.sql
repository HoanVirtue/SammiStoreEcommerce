    UpdatedDate DATETIME NULL,
    CreatedBy NVARCHAR(50),
    UpdatedBy NVARCHAR(50),
    IsActive BIT DEFAULT 1,
    IsDeleted BIT DEFAULT 0,
    DisplayOrder INT DEFAULT 0
);

