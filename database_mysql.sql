CREATE TABLE Users (
    Id INT PRIMARY KEY AUTO_INCREMENT,
    Code VARCHAR(256) NOT NULL,
    Type NVARCHAR(50) CHECK (Type IN ('Customer', 'Employee', 'Supplier')),
